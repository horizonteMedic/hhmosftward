    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        FechaNacimiento = new com.toedter.calendar.JDateChooser();
        String Sql="SELECT d.cod_pa, d.nombres_pa||' '||d.apellidos_pa as nombre, d.fecha_nacimiento_pa ,\n" +
"                n.razon_empresa,n.razon_contrata , n.cargo_de, a.diagnostico as resulexamen,\n" +
"                i.diagnostico, i.fecha_registro,i.diagnostico_audio \n" +
"                FROM datos_paciente AS d \n" +
"               INNER JOIN n_orden_ocupacional AS n ON (d.cod_pa = n.cod_pa) \n" +
"               INNER JOIN audiometria_po AS a ON (a.n_orden = n.n_orden) \n" +
"               INNER JOIN informe_audiometria AS i ON (i.n_orden = n.n_orden) \n" +
"               WHERE n.n_orden ='"+txtNorden.getText()+"' limit 1";
             oConn.FnBoolQueryExecute(Sql);
                try {
                    if (oConn.setResult.next()) {
                        txtNombre.setText(oConn.setResult.getString("nombre"));
                        FechaNacimiento.setDate(oConn.setResult.getDate("fecha_nacimiento_pa"));
                         txtEdad.setText(String.valueOf(oFunc.calcularEdad(FechaNacimiento.getCalendar())) );
                        txtEmpresa.setText(oConn.setResult.getString("razon_empresa")); 
                        txtContrata.setText(oConn.setResult.getString("razon_contrata"));
                        txtDNI.setText(oConn.setResult.getString("cod_pa"));
                        txtCargo.setText(oConn.setResult.getString("cargo_de"));
                      //  txtSexo.setText(oConn.setResult.getString("sexo_pa").equals("M")?"MASCULINO" : "FEMENINO");    
                        
                        txtExamen.setText(oConn.setResult.getString("resulexamen"));
                        atxtDiagnostico.setText(oConn.setResult.getString("diagnostico"));
                         Fecha();
                        txtExamen.setText(oConn.setResult.getString("diagnostico_audio"));
                       }else{
                        oFunc.SubSistemaMensajeError("No se encuentra Registro");
                    }
            } catch (SQLException ex) {
            oFunc.SubSistemaMensajeInformacion("Informe Audiometria:" + ex.getMessage().toString());
        }
    }//GEN-LAST:event_btnEditarActionPerformed
