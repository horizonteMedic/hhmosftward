private boolean Grabar() throws SQLException{
        boolean bResult = false;
              
       String strSqlStmt ="INSERT INTO informe_audiometria(n_orden,diagnostico, fecha_registro,diagnostico_audio)";       
            strSqlStmt+= "values ('"+ txtNorden.getText().toString()+"','"
                    +atxtDiagnostico.getText().toString()+"','"
                    +FechaHoy.getDate()+"','"
                    +txtExamen.getText().toString()+"' )";
           
             if (oConn.FnBoolQueryExecuteUpdate(strSqlStmt)){
                
                   oConn.setResult.next();
            
                    oFunc.SubSistemaMensajeInformacion("Orden Registrada");
                    
                   bResult = true;
               }
              
              
                return bResult;       
        }
