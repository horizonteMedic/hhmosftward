    private void txtNordenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNordenActionPerformed
       if(chkPreocupacional.isSelected()){
        if (!txtNorden.getText().isEmpty()){
            
            if(!OrdenExiste()){

                String Sql="SELECT d.cod_pa, d.nombres_pa||' '||d.apellidos_pa as nombrecompleto,\n" +
"                       d.sexo_pa, d.fecha_nacimiento_pa,d.estado_civil_pa,d.direccion_pa,\n" +
"                        d.nivel_est_pa, n.razon_contrata,  n.razon_empresa\n" +
"                        FROM datos_paciente as d \n" +
"                        INNER JOIN n_orden_ocupacional as n ON (d.cod_pa= n.cod_pa)"
                        + "where n.n_orden ='"+txtNorden.getText()+"'";

                oConn.FnBoolQueryExecute(Sql);
                try {
                    if (oConn.setResult.next()) {

                        txtDNI.setText(oConn.setResult.getString("cod_pa"));
                        txtNombres.setText(oConn.setResult.getString("nombrecompleto"));
                        txtSexo.setText(oConn.setResult.getString("sexo_pa"));
                        FechaNacimiento.setDate(oConn.setResult.getDate("fecha_nacimiento_pa"));
                        txtEstadoCivil.setText(oConn.setResult.getString("estado_civil_pa"));
                        txtDomicilio.setText(oConn.setResult.getString("direccion_pa"));
                        txtEstudios.setText(oConn.setResult.getString("nivel_est_pa"));
                        txtContrata.setText(oConn.setResult.getString("razon_contrata"));
                        txtEmpresa.setText(oConn.setResult.getString("razon_empresa"));
                        txtEdad.setText(String.valueOf(oFunc.calcularEdad(FechaNacimiento.getCalendar())) );
                        txtNorden.setEditable(false);
                        txtNombres.setEditable(false);
                        txtSexo.setEditable(false);
                        FechaNacimiento.setEnabled(false);
                        txtEstadoCivil.setEditable(false);
                        txtDomicilio.setEditable(false);
                        txtEstudios.setEditable(false);
                        txtEmpresa.setEditable(false);
                        
                        oPe.fecha(FechaExamen);

                    }else{
                        oFunc.SubSistemaMensajeError("No se encuentra Algunos Registros necesarios(Alta en Ex-Ocupacionales)");
                    }
                } catch (SQLException ex) {
                    oFunc.SubSistemaMensajeInformacion("Psicosensometrico:" + ex.getMessage().toString());
                }}

            }else{ oFunc.SubSistemaMensajeError("Ingrese N°");}
       
       }else if(chkAsistencial.isSelected()){
           txtNombres.setEditable(true);
           txtSexo.setEditable(true);
           FechaNacimiento.setEnabled(true);
           txtEstadoCivil.setEditable(true);
           txtDomicilio.setEditable(true);
           txtEstudios.setEditable(true);
           txtEmpresa.setEditable(true);
       }
    }//GEN-LAST:event_txtNordenActionPerformed

