    public void mostrar(){
           
 String [] titulos={"Codigo","Nombre de la sede","Direcci√≥n","Comentarios","Estado"};
    String [] registros = new String[5];
    String sql="select cod_sede, nombre_sede, direccion,comentarios, estado from sede;";
      model = new DefaultTableModel(null,titulos);  
                       System.out.println("paso el model");

    if (oConn.FnBoolQueryExecute(sql))
        {
             try  {
                
                while (oConn.setResult.next())
                {
                    registros[0]= oConn.setResult.getString("cod_sede");
                    registros[1]= oConn.setResult.getString("nombre_sede");
                    registros[2]= oConn.setResult.getString("direccion");
                    registros[3]= oConn.setResult.getString("comentarios");
                    registros[4]= oConn.setResult.getString("estado");
                     model.addRow(registros);
                }
                 System.out.println("registro 0"+registros[0]);
                 
                 
                  jTable1.setModel(model);
                
                 oConn.setResult.close();
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, ex);
                oFunc.SubSistemaMensajeError(ex.toString());
                Logger.getLogger(sede.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

}
